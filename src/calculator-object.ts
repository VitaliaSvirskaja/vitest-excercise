function add(a: number, b: number) {
  return a + b;
}

function substract(a: number, b: number) {
  return a - b;
}

function divide(a: number, b: number) {
  return a / b;
}

function multiply(a: number, b: number) {
  return a * b;
}

export const calculator = {
  add: add,
  substract: substract,
  divide: divide,
  multiply: multiply,
};
